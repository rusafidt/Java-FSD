//trim - removes whitespace from both ends
var text1 = "      Hello World!      ";
var text2 = text1.trim();

console.log(text2);

//trimstart - removes whitespace from starting
var text1 = "      Hello World!      ";
var text2 = text1.trimStart();

console.log(text2);


//trimstart - removes whitespace from starting
var text1 = "      Hello World!      ";
var text2 = text1.trimEnd();

console.log(text2);
